package GUI_HR;

import javax.swing.*;
import java.awt.*;

public class ButtonStyle {

    public static void set(JButton b){
        b.setBackground(new Color(0, 0, 128));
        b.setForeground(Color.WHITE);
    }
    public static void setExit(JButton b){
        b.setBackground(new Color(139, 0, 0));
        b.setForeground(Color.WHITE);

    }
}
